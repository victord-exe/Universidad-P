/*
Imprima la siguiente sumatoria w= (x+n)/n!
*/
public class Sumatoria {
    private double x;
    private double n;


    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }
    public double getN() {
        return n;
    }
    public void setN(double n) {
        this.n = n;
    }


    public double CalcularW(){
        double w=0;

        w = (x+n)/-n;
    
        return w;
    }
    
}
